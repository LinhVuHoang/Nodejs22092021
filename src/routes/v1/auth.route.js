const express = require('express'); //import thư viện express
const validate = require('../../middlewares/validate'); //import class validate
const authValidation = require('../../validations/auth.validation'); //import class auth.validation
const authController = require('../../controllers/auth.controller');//import class auth.controller
const auth = require('../../middlewares/auth'); //import class auth

const router = express.Router();
//xuất router
router.post('/register', validate(authValidation.register), authController.register);
//router đăng ký phải thông qua link đến middleware validation.gọi obj registrer trỏ vào biến schema của validate
//thông qua middleware sẽ đến authController
router.post('/login', validate(authValidation.login), authController.login);
//route đăng nhập phải thông qua link đến middleware rồi tiếp đến controller
router.post('/logout', validate(authValidation.logout), authController.logout);
//route đăng xuất tương tự
router.post('/refresh-tokens', validate(authValidation.refreshTokens), authController.refreshTokens);
//route resfress-tokens tương tự
router.post('/forgot-password', validate(authValidation.forgotPassword), authController.forgotPassword);
//route forgor-password tương tự
router.post('/reset-password', validate(authValidation.resetPassword), authController.resetPassword);
//route reset-password tương tự
router.post('/send-verification-email', auth(), authController.sendVerificationEmail);
//route send-verification-email như trên
router.post('/verify-email', validate(authValidation.verifyEmail), authController.verifyEmail);
//route verifi-email tương tự
module.exports = router;

